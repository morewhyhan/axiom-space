/**
 * HyperFrames 渲染测试脚本（纯 JS）
 *
 * 用法：`FFMPEG_PATH=... node scripts/test-hyperframes.mjs`
 */

// 由于 @hyperframes/core 的 ESM 导入在 Node 原生环境下有扩展名解析问题，
// 我们用 Next.js 编译管道来处理。这里直接调用 puppeteer + ffmpeg 做渲染测试。

import puppeteer from 'puppeteer';
import { spawn } from 'node:child_process';
import { writeFile, mkdtemp, unlink } from 'node:fs/promises';
import { join, delimiter } from 'node:path';
import { homedir, tmpdir } from 'node:os';
import { pathToFileURL } from 'node:url';
import { stat } from 'node:fs/promises';

const FFMPEG = process.env.FFMPEG_PATH || 'ffmpeg';

async function sleep(ms) {
  return new Promise(r => setTimeout(r, ms));
}

async function main() {
  console.log('[Test] Starting HyperFrames render test...');
  console.log('[Test] FFmpeg:', FFMPEG);

  // 1. 生成一个简单的自包含 HTML 动画
  const html = `<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>AXIOM HyperFrames</title>
<script src="https://cdnjs.cloudflare.com/ajax/libs/gsap/3.12.5/gsap.min.js"></script>
<style>
  * { margin: 0; padding: 0; box-sizing: border-box; }
  body { background: #000; display: flex; justify-content: center; align-items: center; height: 100vh; }
  #stage { width: 1280px; height: 720px; position: relative; overflow: hidden; background: #1a1a2e; }
  .scene { position: absolute; width: 100%; height: 100%; top: 0; left: 0; }
  .el { position: absolute; font-family: 'Segoe UI', sans-serif; }
</style>
</head>
<body>
<div id="stage">
  <div id="s0" class="scene" style="background:#1a1a2e">
    <div id="t0" class="el" style="left:240px;top:200px;font-size:72px;color:#a855f7;font-weight:bold;opacity:0">AXIOM SPACE</div>
    <div id="t1" class="el" style="left:300px;top:350px;font-size:32px;color:#22d3ee;opacity:0">HyperFrames Video Engine</div>
  </div>
  <div id="s1" class="scene" style="background:#0f0f23;display:none">
    <div id="t2" class="el" style="left:200px;top:150px;font-size:48px;color:#fff;font-weight:bold;opacity:0">AI 驱动 → 知识图谱 → 视频生成</div>
    <div id="t3" class="el" style="left:280px;top:300px;font-size:28px;color:#818cf8;opacity:0">Puppeteer + FFmpeg 实时渲染管线</div>
  </div>
  <div id="s2" class="scene" style="background:#1a1a2e;display:none">
    <div id="t4" class="el" style="left:300px;top:250px;font-size:56px;color:#22c55e;font-weight:bold;opacity:0">Generated by AXIOM</div>
  </div>
</div>
<script>
  const tl = gsap.timeline({ paused: true });
  window.__hfTimeline = tl;

  // Scene 0: 0-3s
  tl.set('#s0', { display:'block' });
  tl.set('#s1', { display:'none' });
  tl.set('#s2', { display:'none' });
  tl.to('#t0', { opacity:1, duration:1 });
  tl.to('#t1', { opacity:1, x:50, duration:1 }, 0.5); // slideIn
  tl.set('#t0', { opacity:0 }, 2.8);

  // Scene 1: 3-7s
  tl.set('#s0', { display:'none' }, 3);
  tl.set('#s1', { display:'block' }, 3);
  tl.set('#t2', { opacity:0, scale:0.8 }, 3);
  tl.set('#t3', { opacity:0, x:-50 }, 3);
  tl.to('#t2', { opacity:1, scale:1, duration:0.8 }, 3);
  tl.to('#t3', { opacity:1, x:0, duration:0.8 }, 3.3);

  // Scene 2: 7-10s
  tl.set('#s1', { display:'none' }, 7);
  tl.set('#s2', { display:'block' }, 7);
  tl.set('#t4', { opacity:0, scale:0.8 }, 7);
  tl.to('#t4', { opacity:1, scale:1, duration:1 }, 7);
</script>
</body>
</html>`;

  const FPS = 24;
  const W = 1280;
  const H = 720;
  const totalFrames = 10 * FPS; // 10秒
  const outputPath = join(tmpdir(), 'axiom-hyperframes-test.mp4');

  // 2. 写临时 HTML
  const tmpDir = await mkdtemp(join(tmpdir(), 'hf-'));
  const htmlPath = join(tmpDir, 'index.html');
  await writeFile(htmlPath, html, 'utf-8');
  console.log('[Test] HTML written to:', htmlPath);

  // 3. 启动浏览器
  const browser = await puppeteer.launch({
    headless: true,
    args: ['--no-sandbox', '--disable-setuid-sandbox', '--disable-dev-shm-usage', '--disable-gpu'],
  });
  console.log('[Test] Browser launched');

  const page = await browser.newPage();
  await page.setViewport({ width: W, height: H });
  await page.goto(pathToFileURL(htmlPath).href, { waitUntil: 'networkidle0', timeout: 30000 });
  await sleep(1000);

  // 4. 启动 FFmpeg
  const homeBin = join(homedir(), 'bin');
  const envPath = [homeBin, process.env.PATH].filter(Boolean).join(delimiter);
  const ffmpeg = spawn(FFMPEG, [
    '-y', '-f', 'image2pipe', '-framerate', String(FPS), '-i', '-',
    '-c:v', 'libx264', '-preset', 'medium', '-crf', '18',
    '-pix_fmt', 'yuv420p', '-movflags', '+faststart',
    outputPath,
  ], {
    stdio: ['pipe', 'inherit', 'inherit'],
    env: { ...process.env, PATH: envPath },
  });

  const startTime = Date.now();

  // 5. 逐帧渲染
  for (let i = 0; i < totalFrames; i++) {
    const t = i / FPS;
    await page.evaluate((time) => {
      const tl = window.__hfTimeline;
      if (tl) tl.seek(time);
    }, t);
    await sleep(30);
    const buf = await page.screenshot({ type: 'png' });
    ffmpeg.stdin.write(buf);

    if (i % 30 === 0) {
      console.log(`[Test] Frame ${i}/${totalFrames} (${Math.round(i/totalFrames*100)}%)`);
    }
  }

  ffmpeg.stdin.end();

  const exitCode = await new Promise(resolve => {
    ffmpeg.on('close', resolve);
    ffmpeg.on('error', e => { console.error('[Test] FFmpeg error:', e); resolve(1); });
  });

  await page.close();
  await browser.close();
  await unlink(htmlPath).catch(() => {});

  if (exitCode !== 0) {
    console.error(`[Test] ❌ FFmpeg exited with code ${exitCode}`);
    process.exit(1);
  }

  const fileSize = (await stat(outputPath)).size;
  const elapsed = Date.now() - startTime;

  console.log(`\n[Test] ✅ Video generated!`);
  console.log(`   Path: ${outputPath}`);
  console.log(`   Size: ${(fileSize / 1024 / 1024).toFixed(2)} MB`);
  console.log(`   Frames: ${totalFrames} @ ${FPS}fps`);
  console.log(`   Time: ${(elapsed / 1000).toFixed(1)}s`);

  process.exit(0);
}

main().catch(err => {
  console.error('[Test] Fatal:', err);
  process.exit(1);
});
